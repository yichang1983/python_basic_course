"""
需求:
    如果年紀小於18,為童工,不合法:
    如果年紀18-60歲之間,為合法工作年紀
    如困年紀大於60為退休年紀
"""

"""
步驟:
    1. 用戶輸入自己的年紀, 保存變量 -- str
    2. if做判 elif
    3. 輸入提示信息: 您輸入的年紀是x, 合法與否
"""

age = int(input('請輸入您的年紀: '))

if age < 18:
    print(f'你的年紀是{age},童工')
elif (age >= 18) and (age <= 60):
    print(f'你的年紀是{age},合法工作')
elif age > 60:
    print(f'你的年紀是{age},退休年紀')

