# int -- 整數
num1 = 1
# float -- 浮點數
num2 = 1.1
print(type(num1))
print(type(num2))

# str
a = "hello world"
print(type(a))

# bool
b = True
print(type(b))

# list
c = [10, 20, 30]
print(type(c))

# tuple
d= (10, 20, 30)
print(type(d))

# set
e = {10, 20, 30}
print(type(e))

# dictionary
f = {'name': 'TOM', 'age': 18}
print(type(f))