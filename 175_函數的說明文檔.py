# help(len)     # help 函數作用： 查看函數的說明文檔（函說的解釋說明的訊息）

# help(len)

def sum_num(a,b):
    """ 函數a+b的和"""
    return a+b

help(sum_num)       # 函數a+b的和

def minus_num(c,d):
    """
    函數c-d的數
    :param c:
    :param d:
    :return:
    """
    return c-d

help(minus_num)     #    函數c-d的數  :param c: :param d: :return:
