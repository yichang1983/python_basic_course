# 1. 多個數據元組
t1 = (10, 20, 30)
print (type(t1))        # <class 'tuple'>

# 2. 單個數據的元組
t2 = (10,)
print(type(t2))         # <class 'tuple'>

# 3.如果單個數據的元組不加逗號
t3 = (10)
print(type(t3))         # <class 'int'>

t4 = ('aaa')
print(type(t4))         # <class 'str'>

t5 = ('aaa',)
print(type(t5))         # <class 'tuple'>